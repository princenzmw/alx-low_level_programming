#include <stdio.h>

/**
 * add - Adds two numbers
 * @a: First number
 * @b: Second number
 * Return: Addition result
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - Substruct b from a
 * @a: First number
 * @b: Second number
 * Return: Subtruction result
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - Multiply two numbers
 * @a: First number
 * @b: Second number
 * Return: Multiplication result
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divide two numbers a by b
 * @a: First number
 * @b: Second number
 * Return: Division result
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - Calculate the reminder of division of two numbers
 * @a: First number
 * @b: Second number
 * Return: Modulo result
 */
int mod(int a, int b)
{
	return (a % b);
}
